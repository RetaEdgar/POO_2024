#Operadores en Python 

#Aritmeticos

#Suma
#x + y
num1=float(input("Coloca el primer numero: "))
num2=float(input("Coloca el segundo numero: "))

print(f"El valor de la suma es: {num1 + num2}")

#Resta
#x - y
print(f"El valor de la suma es: {num1 - num2}")

#multiplicacion
#x * y
print(f"El valor de la suma es: {num1 * num2}")

#Division
#x / y
print(f"El valor de la suma es: {num1 / num2}")

#modulus (mood)
# x % y
print(f"El valor de la suma es: {num1 % num2}")

#Exponente (elevacion)
# x ** y
print(f"El valor de la potencia del primer numero es: {num1 **2}")
print(f"El valor de la potencia del segundo numero es: {num2 **2}")

#floor division (Div)
#x // y
print(f"El valor de la suma es: {num1 // num2}")

#Operadores de comparacion

#Igual
#x == y
print(f"El valor de la suma es: {num1 == num2}")

#Diferente
#x != y
print(f"El valor de la suma es: {num1 != num2}")

#Mayor que
#x > y
print(f"El valor de la suma es: {num1 > num2}")

#Menor que
#x < y
print(f"El valor de la suma es: {num1 < num2}")

#Mayo o igual que
#x >= y
print(f"El valor de la suma es: {num1 >= num2}")

#Menor o igual que
#x <= y
print(f"El valor de la suma es: {num1 <= num2}")




